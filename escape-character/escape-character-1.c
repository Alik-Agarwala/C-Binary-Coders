#include <stdio.h>

int main()
{
    printf("Hello\n"); // new line
    printf("Bye");

    printf("\n\n"); // two times line break

    printf("Hello");
    printf("\nBye");

    printf("\n\n");

    printf("Hello\t"); // horizontal tab
    printf("Bye");

    printf("\n\n");

    printf(" Hello, I am \"Avik\" "); // double quote

    printf("\n\n");

    printf("Hello, I am Avik\a"); // audible alert

    printf("\n\n");

    printf("Hello, I am Avik\\"); // backslash

    return 0;
}